//Degisebilirlik - Mutability
let obje1 = {
    deger: 10
};
let obje2 = obje1;
let obje3 = {
    deger: 10
};
console.log(obje1 === obje2); //True
console.log(obje1 === obje3); //False ikisi farkli nesne cunku, degerleri ayni olsa da
console.log(obje1.deger); //10
console.log(obje2.deger); //10
console.log(obje3.deger); //10
obje1.deger=15;
